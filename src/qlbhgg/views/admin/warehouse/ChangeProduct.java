    private void jButtonSelectLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectLogoActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        String filename = f.getAbsolutePath();
        String extension = "";
        int i = filename.lastIndexOf('.');
        if (i >= 0) {
            extension = filename.substring(i+1);
        }
        if(extension.equals("png") || extension.equals("jpg")) {
            String[] parts = filename.split("\\\\");
            boolean check = false;
            for(String item : parts) {
                if(item.equals("ImageProduct") || check) {
                    path += "/" + item;
                    check = true;
                }
            }
            ImageIcon icon = new ImageIcon(filename);
            Image image = icon.getImage().getScaledInstance(jLabel9.getWidth(), jLabel9.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon scaledIcon = new ImageIcon(image);
            jLabel9.setIcon(scaledIcon);
        } else {
            Helper helper = new Helper();
            helper.sendMessage("Vui lòng chọn định dạng file png hoặc jpg");
            return;
        }
        
    }//GEN-LAST:event_jButtonSelectLogoActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String productCode = this.productCode;
            int ret = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa sản phẩm này không ?", "Confirm", JOptionPane.YES_NO_OPTION);
            if(ret != JOptionPane.YES_OPTION) {
                return;
            }
            ProductController pc = new ProductController();
            pc.deleteProduct(productCode);
            Helper helper = new Helper();
            helper.sendMessage("Đã xóa sản phẩm");
            this.dispose();
            pw.update();
        } catch (SQLException ex) {
            Logger.getLogger(ChangeProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

