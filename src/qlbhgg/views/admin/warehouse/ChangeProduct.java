    private void jButtonSaveSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveSupplierActionPerformed
        Helper helper = new Helper();
        if(productName.getText().equals("") || productAmount.getText().equals("") || productUnit.getText().equals("") || productImport.getText().equals("") || productPrice.getText().equals("")) {
            helper.sendMessage("Vui lòng nhập đầy đủ thông tin");
            return;
        }
        if(path.equals("")) {
            helper.sendMessage("Vui lòng chọn ảnh !!!");
            return;
        }
        if(!helper.isFloatNumber(productImport.getText()) || !helper.isFloatNumber(productPrice.getText()) || !helper.isIntNumber(productAmount.getText())) {
            helper.sendMessage("Vui lòng nhập đúng định dạng số");
            return;
        }
        try {
            ProductController pc = new ProductController();
            switch(key) {
                case "add":
                    pc.addProduct(this);
                    helper.sendMessage("Thêm sản phẩm thành công");
                    pw.update();
                    this.dispose();
                    break;
                case "update":
                    pc.updateProduct(this, productCode);
                    helper.sendMessage("Cập nhật sản phẩm thành công");
                    pw.update();
                    this.dispose();
                    break;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ChangeProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSaveSupplierActionPerformed

