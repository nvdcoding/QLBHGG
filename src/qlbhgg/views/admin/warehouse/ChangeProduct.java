    public Goods getProduct() {
        Goods product = new Goods();
        int indexSup = suppliers.getSelectedIndex();
        int indexType = listT.getSelectedIndex();
        product.setItemname(productName.getText());
        product.setCompanycode(list.get(indexSup).getCompanycode());
        product.setTypecode(listTog.get(indexType).getTypecode());
        product.setAmount(Integer.parseInt(productAmount.getText()));
        product.setUnit(productUnit.getText());
        product.setImportprice(Float.parseFloat(productImport.getText()));
        product.setPrice(Float.parseFloat(productPrice.getText()));
        product.setImageitem(path);
        return product;
    }
    private void setComboBox() throws SQLException {
        
        for (Suppliers s : list) {
            suppliers.addItem(s.getCompanyname());
        }
        for (TypeOfGoods t : listTog) {
            listT.addItem(t.getTypename());
        }
    }
    private void setProduct(String code) throws SQLException {
        ProductController productController = new ProductController();
        
        Goods product = new Goods();
        product = productController.getProduct(code);
        String supplier = sc.getSupplier(product.getCompanycode());
        String type = tc.getType(product.getTypecode());
        productName.setText(product.getItemname());
        productAmount.setText(product.getAmount() + "");
        productUnit.setText(product.getUnit());
        productImport.setText(product.getImportprice() + "");
        productPrice.setText(product.getPrice() + "");
        path = product.getImageitem();
        ImageIcon icon = new ImageIcon(getClass().getResource(product.getImageitem()));
        Image image = icon.getImage().getScaledInstance(jLabel9.getWidth(), jLabel9.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(image);
        jLabel9.setIcon(scaledIcon);
        
        suppliers.setSelectedItem(supplier);
        listT.setSelectedItem(type);
    }
