    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        Helper helper = new Helper();
        if (isEmpty()) {
            lbNotice.setText("Vui lòng nhập đầy đủ thông tin");
            return;
        }
        if (!helper.validateMail(customerEmail.getText())) {
            lbNotice.setText("Vui lòng nhập đúng định dạng email !!!");
            return;
        }
        try {
            CustomerController customerController = new CustomerController();
            switch (key) {
                case "add":
                    customerController.addCustomer(this);
                    helper.sendMessage("Thêm khách hàng thành công");
                    pc.updateTable();
                    this.dispose();
                    break;
                case "update":
                    customerController.updateCustomer(this, customerCode);
                    helper.sendMessage("Cập nhật khách hàng thành công");
                    pc.updateTable();
                    this.dispose();
                    ;
                    break;
                default:
                    break;

            }
        } catch (SQLException ex) {
            Logger.getLogger(ChangeCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_confirmBtnActionPerformed

