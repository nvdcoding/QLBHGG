    public Customers getCustomer() {
        Customers customer = new Customers();
        customer.setCustomername(customerName.getText());
        customer.setAddress(customerAddress.getText());
        customer.setEmail(customerEmail.getText());
        customer.setPhonenumber(customerPhone.getText());
        customer.setTradingname(content.getText());
        return customer;
    }

    private boolean isEmpty() {
        if (customerName.getText().equals("") || customerAddress.getText().equals("") || customerEmail.getText().equals("") || customerPhone.getText().equals("") || content.getText().equals("")) {
            return true;
        }
        return false;
    }

    private void checkAction(String customerCode) {
        if (key.equals("update")) {
            try {
                CustomerController customerController = new CustomerController();
                Customers customer = customerController.getOneById(customerCode);
                customerName.setText(customer.getCustomername());
                customerAddress.setText(customer.getAddress());
                customerEmail.setText(customer.getEmail());
                customerPhone.setText(customer.getPhonenumber());
                content.setText(customer.getTradingname());
            } catch (SQLException ex) {
                Logger.getLogger(ChangeCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

